package com.doganilbars.main;

public class PostIncrement {

	public static void main(String[] args) {
		int number = 10;
		// þimdi sayý deðiþmez deðeri halen 10
		System.out.println("Now number: " + (number++));// this is post increment
		// þimdi sayý 1 artar 11 olur
		System.out.println("Now number: " + number);
	}

}
