package com.doganilbars.main;

public class ForLoop1 {

	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {
			System.out.print(i + " ");
		}

		// bir satýr aþaðýya insin diye System.out.println();
		System.out.println();

		for (int i = 0; i <= 10; ++i) {
			System.out.print(i + " ");
		}
	}

}
